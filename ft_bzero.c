#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ch;

	i = 0;
	ch = (unsigned char*)s;
	while (i++ < n)
	{
		*ch = 0;
		ch++;
	}
}
